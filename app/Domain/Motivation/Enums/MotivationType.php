<?php

namespace App\Domain\Motivation\Enums;

use App\Domain\Common\Traits\HasEnumValues;

enum MotivationType: string
{
    use HasEnumValues;

    case MOTIVATIONAL = 'motivational';
    case JOKE = 'joke';
    case FACT = 'fact';
    case QUOTE = 'quote';

    public function label(): string
    {
        return match($this) {
            self::MOTIVATIONAL => 'Мотивационное',
            self::JOKE => 'Шутка',
            self::FACT => 'Факт',
            self::QUOTE => 'Цитата',
        };
    }

    public function prompt(): string
    {
        return match($this) {
            self::MOTIVATIONAL => 'Напиши короткую необычную мотивационную фразу (до 100 символов) для пользователя, который авторизовался в системе управления задачами. Будь креативным, оригинальным и вдохновляющим. Используй нестандартные метафоры, иронию или легкий сарказм, но оставайся позитивным. Избегай банальностей и клише.',
            self::JOKE => 'Напиши короткую остроумную шутку (до 100 символов) про работу, задачи, дедлайны или продуктивность. Можно использовать черный юмор, сарказм или иронию, но в рамках приличий - без оскорблений, дискриминации и запрещенного контента. Будь креативным и необычным.',
            self::FACT => 'Напиши интересный неожиданный факт (до 100 символов) про продуктивность, управление временем, работу, мозг или психологию. Будь познавательным, но не скучным. Можно добавить легкую иронию или необычный ракурс.',
            self::QUOTE => 'Напиши короткую мудрую цитату (до 100 символов) про успех, работу, достижения или жизнь. Можешь использовать сарказм, иронию или нестандартный подход, но оставайся вдохновляющим. Избегай банальных фраз.',
        };
    }
}

